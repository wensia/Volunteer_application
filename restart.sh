#!/bin/bash

# 天津中考位次查询系统重启脚本
# 先停止后端服务，然后重新启动

echo "==================================="
echo "天津中考位次查询系统 - 重启服务"
echo "==================================="
echo ""

# 停止现有服务
echo "🛑 正在停止现有服务..."
./stop.sh

echo ""
echo "⏱️  等待2秒后重新启动..."
sleep 2

echo ""
echo "🚀 正在启动服务..."
echo "==================================="

# 启动服务
./start.sh 
# 按区域分组的学校数据
SCHOOLS_BY_DISTRICT = {}
for school in ADMISSION_DATA_2024:
    district = school["区域"]
    if district not in SCHOOLS_BY_DISTRICT:
        SCHOOLS_BY_DISTRICT[district] = []
    SCHOOLS_BY_DISTRICT[district].append(school)

# 获取学校数据的辅助函数
def get_school_by_name(school_name):
    """根据学校名称获取学校数据"""
    for school in ADMISSION_DATA_2024:
        if school["学校名称"] == school_name:
            return school
    return None

def get_schools_by_score_range(min_score, max_score, year=2024):
    """获取指定分数区间的学校"""
    result = []
    score_key = f"{year}年录取分数线"
    for school in ADMISSION_DATA_2024:
        score = school.get(score_key, 0)
        if min_score <= score <= max_score:
            result.append(school)
    return result

